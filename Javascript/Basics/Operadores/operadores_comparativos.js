/* Se conocen como operadores comparativos a los simbolos que nos permiten 
comparar dos valores */

// Operador de igualdad (==)

console.log(5 == 5)
console.log(5 == '5')

console.log("comparando textos")
console.log("hola" == "hola")

// Operador de igualdad estricta (===)

console.log(5 === 5)
console.log(5 === '5')
console.log("comparando textos")
console.log("hola" === "Hola")

// Operador de desigualdad (!=)

console.log(5 != 3)
console.log(5 != 5)
console.log("comparando textos")
console.log("usuario" != "invitado")

// Operador de desigualdad estricta (!==)

console.log(5 !== 3)
console.log(5 !== 5)
console.log(5 !== '5')

// Operador mayor que (>)

console.log(5 > 3)
console.log(3 > 5)
console.log(5 > 5)

// Operador mayor o igual que (>=)
console.log(5 >= 3)
console.log(5 >= 5)
console.log(3 >= 5)

// Operador menor que (<)

console.log(5 < 3)
console.log(3 < 5)

// Operador menor o igual que (<=)
console.log(5 <= 3)
console.log(5 <= 5)
console.log(3 <= 5)